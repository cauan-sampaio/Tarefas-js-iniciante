/**
 * Aritméticos
 * + Adição / Concatenação
 * ** Potenciação
 * % Resto da divisão
 * Incremetno ++
 * Decremento --
 * parseInt (inteiro), parseFloat (quebrado)
 */

const num1 = 10;
const num2 = 2;
const num3 = 4;
console.log((num1 + num2) * num3);
let n1 = 1;
console.log(++n1);
let n2 = 4;
console.log(--n2);
let contador = 10;
contador += 0;
console.log(contador);
const n4 = 3;
const n5 = parseInt('2');
